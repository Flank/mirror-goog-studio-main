package com.android.tools.deploy.liveedit;

import java.util.LinkedList;
import java.util.Queue;

public class InvokeInterface {

    public static int callInterface() {
        Queue q = new LinkedList<>();
        q.add(new Object());
        q.clear();
        return q.size();
    }
}
